    private final DefaultTableCellRenderer colorTableRenderer = new DefaultTableCellRenderer() {

        @Override
        public Component getTableCellRendererComponent(
                JTable table,
                Object value,
                boolean isSelected,
                boolean hasFocus,
                int row,
                int column) {
            Object c = table.getValueAt(row, 1);
            NamedColor namedColor = null;
            if (c instanceof NamedColor namedColorCast) {
                namedColor = namedColorCast;

            }
            Component component = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (isSelected) {
                component.setForeground(table.getSelectionForeground());
                component.setBackground(table.getSelectionBackground());
            } else {
                if (namedColor != null) {
                    if (!namedColor.isDefaultColor()) {
                        component.setForeground(namedColor.getTextColor());
                        component.setBackground(namedColor);
                    } else {
                        if (namedColor.getColor().equals(table.getBackground())) {
                            component.setForeground(table.getForeground());
                        } else {
                            component.setForeground(NamedColor.getTextColor(table.getForeground()));
                        }
                        component.setBackground(table.getBackground());
                    }
                } else {
                    component.setForeground(table.getForeground());
                    component.setBackground(table.getBackground());
                }
            }
            return component;
        }
    };

    private final BasicComboBoxRenderer colorComboBoxRenderer = new BasicComboBoxRenderer() {

        @Override
        public Component getListCellRendererComponent(
                JList list,
                Object value,
                int index,
                boolean isSelected,
                boolean cellHasFocus) {

            NamedColor c = null;
            if (value instanceof NamedColor namedColor) {
                c = namedColor;
                value = c.getText();
            }
            Component component = super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
            if (c != null) {
                component.setBackground(c);
                component.setForeground(c.getTextColor());
            } else {
                component.setForeground(list.getForeground());
                component.setBackground(list.getBackground());
            }
            return component;
        }
    };


    public static javax.swing.ImageIcon getCategoryIcon(FilterProperty.FilterCategory category) {
        switch (category)  {
            case HTTP:
                return image_http;
            case WEBSOCKET:
                return image_websocket;
            case SITE_MAP:
                return image_site_map;
        }
        return null;
    }
    
